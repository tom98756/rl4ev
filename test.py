class 计数器:
    def __init__(self, 初始值=0):
        self.计数 = 初始值
    
    def 增加(self, 数量=1):
        self.计数 += 数量
    
    def 减少(self, 数量=1):
        self.计数 -= 数量
    
    def 获取计数(self):
        return self.计数
    
    def 重置(self, 新值=0):
        self.计数 = 新值

# 使用示例
计数器实例 = 计数器()
计数器实例.增加()
print(计数器实例.获取计数())  # 输出: 1
计数器实例.增加(5)
print(计数器实例.获取计数())  # 输出: 6
计数器实例.减少(2)
print(计数器实例.获取计数())  # 输出: 4
计数器实例.重置()
print(计数器实例.获取计数())  # 输出: 0
